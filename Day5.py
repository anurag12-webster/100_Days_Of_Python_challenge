'''Day 5 coding Statement:  Write a program to identify if the number is even or odd

Description

Get a number as input from the user and check whether the given number is odd or even
'''
Number = int(input('Enter a value: '))
if Number %2==0:
    print('Even')
else:
    print('Odd')